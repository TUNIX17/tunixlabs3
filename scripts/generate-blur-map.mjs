#!/usr/bin/env node
/**
 * generate-blur-map.mjs
 * Reads public/cases/{slug}/01.webp.blur.json sidecars and emits
 * src/generated/blurMap.ts with { [caseCmd]: blurDataURL }.
 *
 * Keyed by the CaseData.cmd string (e.g. "./cases --show apoderapp") so
 * the v3/page.tsx lookup can be done as blurMap[c.cmd].
 *
 * Run: node scripts/generate-blur-map.mjs
 *
 * Cases without a sidecar (e.g. gasco) are omitted — v3/page.tsx falls
 * back to placeholder='empty' when the key is missing.
 */

import { readFileSync, readdirSync, existsSync, mkdirSync, writeFileSync } from 'node:fs';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);
const repoRoot = join(__dirname, '..');
const casesDir = join(repoRoot, 'public', 'cases');
const outDir = join(repoRoot, 'src', 'generated');
const outFile = join(outDir, 'blurMap.ts');

if (!existsSync(casesDir)) {
  console.error('[blurMap] public/cases not found:', casesDir);
  process.exit(1);
}

const slugs = readdirSync(casesDir, { withFileTypes: true })
  .filter((d) => d.isDirectory())
  .map((d) => d.name);

const entries = [];
for (const slug of slugs) {
  const sidecar = join(casesDir, slug, '01.webp.blur.json');
  if (!existsSync(sidecar)) {
    console.log(`[blurMap] skip ${slug} (no sidecar)`);
    continue;
  }
  try {
    const json = JSON.parse(readFileSync(sidecar, 'utf8'));
    if (!json.blurDataURL || typeof json.blurDataURL !== 'string') {
      console.warn(`[blurMap] ${slug}: sidecar missing blurDataURL`);
      continue;
    }
    // Key matches CaseData.cmd format in v3/page.tsx
    entries.push([`./cases --show ${slug}`, json.blurDataURL]);
  } catch (err) {
    console.warn(`[blurMap] ${slug}: failed to parse sidecar:`, err.message);
  }
}

entries.sort(([a], [b]) => a.localeCompare(b));

const header = `// AUTO-GENERATED by scripts/generate-blur-map.mjs
// Do not edit by hand. Run: node scripts/generate-blur-map.mjs
// Keyed by CaseData.cmd (e.g. "./cases --show apoderapp").
// Cases without a sidecar are omitted (page falls back to placeholder='empty').

export const blurMap: Record<string, string> = {
`;

const body = entries
  .map(([k, v]) => `  ${JSON.stringify(k)}: ${JSON.stringify(v)},`)
  .join('\n');

const footer = `\n};\n`;

if (!existsSync(outDir)) mkdirSync(outDir, { recursive: true });
writeFileSync(outFile, header + body + footer, 'utf8');

console.log(`[blurMap] wrote ${entries.length} entries -> ${outFile}`);
